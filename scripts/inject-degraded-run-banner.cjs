const fs = require('fs');
const path = require('path');
const root = path.join(__dirname, '..');
const indexPath = path.join(root, 'index.html');
if (!fs.existsSync(indexPath)) throw new Error('index.html missing');
const banner = `<section id="run-mode-banner" class="panel run-mode-banner"><div><p class="eyebrow">Run mode</p><h2>Local intelligence mode</h2></div><p>Signals are derived from market data, saved filings/evidence, and the current portfolio state. Fresh news ingestion is not active for this run.</p></section>`;
const css = `<style>.run-mode-banner{display:grid;grid-template-columns:minmax(220px,.55fr) 1.45fr;gap:18px;align-items:center;border-color:rgba(138,106,44,.22);background:rgba(138,106,44,.045)}.run-mode-banner h2{margin:0;font-size:24px;line-height:1;letter-spacing:-.04em}.run-mode-banner p:last-child{margin:0;color:rgba(36,35,31,.68);font-size:14px;line-height:1.42}@media(max-width:760px){.run-mode-banner{grid-template-columns:1fr}}</style>`;
let html = fs.readFileSync(indexPath, 'utf8');
html = html.replace(/<style>\.run-mode-banner[\s\S]*?<\/style>/, '');
html = html.replace(/<section id="run-mode-banner"[\s\S]*?<\/section>/, '');
html = html.replace('</head>', `${css}</head>`);
const anchor = '<div class="topbar"';
if (html.includes(anchor)) html = html.replace(anchor, `${banner}${anchor}`);
else html = html.replace(/(<main[^>]*>)/, `$1${banner}`);
fs.writeFileSync(indexPath, html);
console.log('injected local intelligence mode banner');
