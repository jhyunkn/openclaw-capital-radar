const fs=require('fs');
const path=require('path');
const root=path.join(__dirname,'..');
const htmlPath=path.join(root,'index.html');
const statePath=path.join(root,'outputs','duration-state.json');
const esc=v=>String(v??'').replace(/[&<>"']/g,c=>({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
const arr=v=>Array.isArray(v)?v:[];
const state=fs.existsSync(statePath)?JSON.parse(fs.readFileSync(statePath,'utf8')):{};
const anns=arr(state?.annotation_spec?.charts?.duration_main);
const suppressed=arr(state?.annotation_spec?.suppressed).length;
const rows=anns.slice(-4).reverse();
let html=fs.readFileSync(htmlPath,'utf8');
html=html.replace(/<div class="duration-evidence-banner">[\s\S]*?<\/div>\s*/g,'');
html=html.replace(/<style>\.duration-evidence-banner[\s\S]*?<\/style>/g,'');
const style='<style>.duration-evidence-banner{border:1px solid rgba(164,80,47,.55);border-radius:20px;background:rgba(164,80,47,.09);padding:14px;margin:10px 0}.duration-evidence-banner span{display:block;color:var(--muted);font-size:9px;text-transform:uppercase;letter-spacing:.1em}.duration-evidence-banner b{display:block;font-size:18px;font-weight:500;margin-top:4px}.duration-evidence-banner ul{display:grid;grid-template-columns:repeat(auto-fit,minmax(170px,1fr));gap:6px;margin:10px 0 0;padding:0;list-style:none}.duration-evidence-banner li{border-top:1px solid var(--rule);padding-top:7px;font-size:12px}.duration-evidence-banner small{display:block;color:var(--muted);font-size:10px;margin-top:2px}</style>';
const list=rows.length?`<ul>${rows.map(a=>`<li>${esc(a.label)}<small>${esc(a.date_start||'')}${a.date_end?` → ${esc(a.date_end)}`:''}</small></li>`).join('')}</ul>`:'<small>No supported Duration set points yet.</small>';
const banner=`<div class="duration-evidence-banner"><span>Duration evidence · Mission 5B</span><b>${anns.length} supported historical set points · ${suppressed} suppressed</b>${list}</div>`;
html=html.replace('</head>',style+'</head>');
const macroHead=html.indexOf('<section id="decision-brief-section"');
if(macroHead<0)throw new Error('decision-brief-section missing');
const insertAfter=html.indexOf('</h2>',macroHead);
if(insertAfter>=0)html=html.slice(0,insertAfter+5)+banner+html.slice(insertAfter+5);
else html=html.replace('<section id="decision-brief-section"','<section id="decision-brief-section"'+banner);
fs.writeFileSync(htmlPath,html);
console.log('injected Duration evidence banner into Macro top');
